    public void Update_table() {

        try {
            String sql = "select * from Staff_information";
            pstm = con.prepareStatement(sql);
            rs = pstm.executeQuery();
            System.out.println();
            view_table.setModel(DbUtils.resultSetToTableModel(rs));

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        } finally {
            try {
                rs.close();
                pstm.close();
            } catch (Exception e) {
            }
        }

    }
    
    void Update_lable(){
        try {
            
            String sql = "select count(id) from Staff_information";
            pstm = con.prepareStatement(sql);
            rs = pstm.executeQuery();
            if(rs.next()){
                String sum = rs.getString("count(id)");
                lbl_TE.setText(sum);
            }
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
        finally{
            try {
                pstm.close();
                rs.close();
            } catch (Exception e) {
            }
        }
    }

